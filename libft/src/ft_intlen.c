#include "libft.h"

int		ft_intlen(long long n)
{
	int i;

	i = 0;
	(n <= 0) ? i++ : i;
	n = (n < 0) ? (n * -1) : n;
	while (n > 0)
	{
		n /= 10;
		i++;
	}
	return (i);
}
