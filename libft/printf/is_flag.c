#include <ft_printf.h>

int	is_flag(char c)
{
	if (c == 'd' || c == 'D' || c == 'i' || c == 'u' || c == 'U' || c == 'o'\
		|| c == 'O' || c == 'x' || c == 'X' || c == 'f' || c == 'F' || c == 'e'\
		|| c == 'E' || c == 'g' || c == 'G' || c == 'c' || c == 'C' || c == 's'\
		|| c == 'S' || c == 'p' || c == 'n' || c == '%')
		return (1);
	else if (c == 'h' || c == 'l' || c == 'j' || c == 'z' || c == 't'\
	|| c == 'L')
		return (2);
	else if (c == '-' || c == '+' || c == '#' || c == '.' || c == ' '\
	|| c == '*' || ft_isdigit(c) || c == '\'' || c == '$')
		return (3);
	return (0);
}
